    public void MostrarDatosTabla() {
        DefaultTableModel ObjModel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        ObjModel.addColumn("Patente");
        ObjModel.addColumn("Marca");
        ObjModel.addColumn("Modelo");
        ObjModel.addColumn("Año");
        ObjModel.addColumn("Kilometraje");
        ObjModel.addColumn("Estado");

        TblDatos.setModel(ObjModel);

        ResultSet resultado = null;
        Connection con = null;
        try {
            resultado = CamionesCrud.listatTodos();
            if (resultado != null) {
                con = resultado.getStatement().getConnection();

            }

            int cantRegistros = 0;
            while (resultado != null && resultado.next()) {
                ObjModel.addRow(new Object[]{
                    resultado.getString("idCamion"),
                    resultado.getString("marca"),
                    resultado.getString("modelo"),
                    resultado.getString("anio"),
                    resultado.getString("kilometrajeActual"),
                    resultado.getString("estadoMantenimiento"),});

                cantRegistros++;

            }

            LblTotalUsuarios.setText("Total Camiones: " + cantRegistros);

        } catch (SQLException e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "No se pudieron cargar los datos", "Alerta", JOptionPane.WARNING_MESSAGE);
        }

    }

