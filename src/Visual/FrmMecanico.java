    private void TxtPatenteKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtPatenteKeyTyped
        char c = evt.getKeyChar();

        // Convertir a mayúscula automáticamente
        evt.setKeyChar(Character.toUpperCase(c));

        // Permitir solo letras, números y guion
        if (!Character.isLetterOrDigit(c) && c != '-') {
            evt.consume(); // bloquea el caracter
        }
    }//GEN-LAST:event_TxtPatenteKeyTyped

    //modificar campos para buscar
    private void BtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarActionPerformed
        String Patente = TxtPatente.getText();
        if (Patente.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingresa una patente para buscar", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            Camion c = CamionesCrud.buscar(Patente);
            if (c != null) {
                TxtPatente.setText(c.getIdCamion());
                TxtMarca.setText(c.getMarca());
                TxtModelo.setText(c.getModelo());
                TxtAnio.setText(Integer.toString(c.getAnio()));
                TxtKilometraje.setText(Integer.toString(c.getKilometrajeActual()));
                CmbEstado.setSelectedItem(c.getEstadoMantenimiento());

            } else {
                JOptionPane.showMessageDialog(null, "Camion no encontrado", "Advertencia", JOptionPane.WARNING_MESSAGE);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Faltan campos por completar", "Advertencia", JOptionPane.WARNING_MESSAGE);

        }
    }//GEN-LAST:event_BtnBuscarActionPerformed

    private void BtnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnModificarActionPerformed
 
            
        String Patente = TxtPatente.getText();
        if (Patente.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor ingresa una patente", "Alert", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String Marca = TxtMarca.getText();
        String Modelo = TxtModelo.getText();
        String AnioText = TxtAnio.getText();
        String Estado = (String) CmbEstado.getSelectedItem();
        String KilometrajeText = TxtKilometraje.getText();

        if (KilometrajeText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Faltan campos por completar", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        } 
        
        if ("Administrador".equalsIgnoreCase(tipoUsuario)) {
            if (Marca.isEmpty() || Modelo.isEmpty() || AnioText.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Faltan campos por completar", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }
        }
    
        int Kilometraje = Integer.parseInt(KilometrajeText);
        int Anio = Integer.parseInt(AnioText);


        if ("Administrador".equalsIgnoreCase(tipoUsuario)) {

            DlgVerificacion dlg = new DlgVerificacion(this, true, tipoUsuario);
            dlg.setVisible(true);

            if (!dlg.isVerificado()) {
                JOptionPane.showMessageDialog(this, "Verificación cancelada");
                return;
            }
        }
        c.setIdCamion(Patente);
        c.setMarca(Marca);
        c.setModelo(Modelo);
        c.setAnio(Anio);
        c.setKilometrajeActual(Kilometraje);
        c.setEstadoMantenimiento(Estado);

        CamionesCrud.actualizar(c);
        MostrarDatosTabla();
        validarKilometraje(Kilometraje);

    }//GEN-LAST:event_BtnModificarActionPerformed

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        
        DlgVerificacion dlg = new DlgVerificacion(this, true, tipoUsuario);
        dlg.setVisible(true);
        
        if(dlg.isVerificado()){
            String id = TxtPatente.getText();
            if (id.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Ingresa un rut para eliminar", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int Confirm = JOptionPane.showConfirmDialog(this, "Estas seguro de eliminar a este Empleado?", "Confirmacion", JOptionPane.YES_NO_OPTION);

            if (Confirm == JOptionPane.YES_OPTION) {
                CamionesCrud.eiliminar(id);

            }
            MostrarDatosTabla();
        }else {
            JOptionPane.showMessageDialog(this, "Accion Cancelada","Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

