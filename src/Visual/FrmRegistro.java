    private void BtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarActionPerformed
        String Rut = TxtRut.getText();
        if(Rut.isEmpty()){
            JOptionPane.showMessageDialog(null,"Ingresa un rut para buscar","Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            Usuario u = UsuariosCrud.buscar(Rut);
            if(u != null){
                TxtRut.setText(u.getRut());
                TxtNombre.setText(u.getNombre());
                TxtApellido.setText(u.getApellido());
                TxtClave.setText(u.getClave());
                CmbCamiones.setSelectedItem(u.getIdCamion());
                CmbTipoUsuario.setSelectedItem(u.getTipoUsuario());


            }else{
                JOptionPane.showMessageDialog(null,"Empleado no encontrado","Advertencia", JOptionPane.WARNING_MESSAGE);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Faltan campos por completar","Advertencia", JOptionPane.WARNING_MESSAGE);

        }
    }//GEN-LAST:event_BtnBuscarActionPerformed

