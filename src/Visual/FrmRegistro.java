    public void cargarCamiones() {
        CmbCamiones.removeAllItems();

        try {
            ResultSet resultado = UsuariosCrud.listarCamiones();

            // equivalente a NULL
            CmbCamiones.addItem("Sin camion asignado");

            while (resultado != null && resultado.next()) {
                String idCamion = resultado.getString("idCamion");
                CmbCamiones.addItem(idCamion);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar camiones");
        }
    }

    
     public void MostrarDatosTabla(){
        DefaultTableModel ObjModel = new DefaultTableModel() {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; 
            }
        };
        
        ObjModel.addColumn("Rut");
        ObjModel.addColumn("Clave");
        ObjModel.addColumn("Nombre");
        ObjModel.addColumn("Apellido");
        ObjModel.addColumn("idCamion");
        ObjModel.addColumn("Cargo");
        
        TblDatos.setModel(ObjModel);
        
        ResultSet resultado = null;
        Connection con = null;
        try {
            resultado = UsuariosCrud.listatTodos();
            if (resultado != null) {
                con = resultado.getStatement().getConnection();
                
            }
            
            int cantRegistros = 0;
            while (resultado != null && resultado.next()) {
                ObjModel.addRow(new Object[]{
                resultado.getString("Rut"),
                resultado.getString("Contrasena"),
                resultado.getString("Nombre"),
                resultado.getString("Apellido"),
                resultado.getString("idCamion"),
                resultado.getString("Cargo"),
                
                });
                
                cantRegistros ++;
                
            }
     
           LblTotalUsuarios.setText("Total usuarios: "+cantRegistros);
                
           
            
            
        } catch (SQLException e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this,"No se pudieron cargar los datos","Alerta", JOptionPane.WARNING_MESSAGE);
        }
                
    }
     
