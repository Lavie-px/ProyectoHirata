    private void BtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegistrarActionPerformed
        String Rut = TxtRut.getText();
        //String dv = TxtVerificador.getText();
        String Nombre = TxtNombre.getText();
        String Apellido = TxtApellido.getText().trim();
        String Clave = TxtClave.getText();

        String TipoUsuario = (String)CmbTipoUsuario.getSelectedItem();
        String idCamion = (String)CmbCamiones.getSelectedItem();
        if(Rut.isEmpty()||Nombre.isEmpty() || Apellido.isEmpty() || Clave.isEmpty() ){
            JOptionPane.showMessageDialog(null,"Faltan campos por completar","Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        u.setRut(Rut);
        u.setNombre(Nombre);
        u.setApellido(Apellido);
        u.setClave(Clave);
        u.setIdCamion(idCamion);
        u.setTipoUsuario(TipoUsuario);
        
        UsuariosCrud.grabar(u);
        MostrarDatosTabla();
    }//GEN-LAST:event_BtnRegistrarActionPerformed

