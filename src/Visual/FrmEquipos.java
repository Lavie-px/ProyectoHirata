    private void BtnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnActualizarActionPerformed
        String idEquipo = TxtIdEquipo.getText();
        String fecha = TxtFecha.getText();
        if (idEquipo.isEmpty() || fecha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Campo Id o Fecha vacios");
            return;
        }
        Detalles d = new Detalles();

        d.ram = ChkRam.isSelected();
        d.procesador = ChkProcesador.isSelected();
        d.fuente = ChkFuente.isSelected();
        d.placaBase = ChkPlaca.isSelected();
        d.disipador = ChkDisipador.isSelected();

        d.almacenamiento = new Detalles.Almacenamiento();
        d.almacenamiento.ssd = ChkSsd.isSelected();
        d.almacenamiento.hdd = ChkHdd.isSelected();

        d.perifericos = new Detalles.Perifericos();
        d.perifericos.monitor = ChkMonitor.isSelected();
        d.perifericos.mouse = ChkMouse.isSelected();
        d.perifericos.teclado = ChkTeclado.isSelected();

        if (RdoPreventivo.isSelected()) {
            d.tipoMantenimiento = "PREVENTIVO";
        } else if (RdoCorrectivo.isSelected()) {
            d.tipoMantenimiento = "CORRECTIVO";
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona tipo de mantenimiento");
            return;
        }

        d.descripcion = TxtAreaDetalle.getText();

        try {
            ObjectMapper mapper = new ObjectMapper();
            String json = mapper.writeValueAsString(d);
            java.sql.Date fechaFormat = java.sql.Date.valueOf(fecha);
            crud.actualizar(idEquipo, json, fechaFormat);
            
            ResultadoMantenimiento res = crud.buscar(idEquipo,fechaFormat);

            TxtAreaMantenimiento.setText(ResumenDetalle(idEquipo, d, (Date) res.fecha, res.hora));

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al actualizar mantenimiento");
        }
    }//GEN-LAST:event_BtnActualizarActionPerformed

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        String idEquipo = TxtIdEquipo.getText();
        String fecha = TxtFecha.getText();
        if (idEquipo.isEmpty()||fecha.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Campo Id o Fecha vacios");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
            this,
            "¿Estás seguro de eliminar el mantenimiento del equipo " + idEquipo + "?",
            "Confirmación",
            JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        java.sql.Date fechaFormat = java.sql.Date.valueOf(fecha);
        CrudMantenimiento crud = new CrudMantenimiento();
        crud.eiliminar(idEquipo, fechaFormat);

        limpiarUi();
        TxtAreaMantenimiento.setText("");
    }//GEN-LAST:event_BtnEliminarActionPerformed

