    private String ResumenDetalle(String idEquipo, Detalles d, java.sql.Date fecha, java.sql.Time hora) {

        StringBuilder sb = new StringBuilder();

        sb.append("=== MANTENIMIENTO DE EQUIPO ===\n\n");

        sb.append("ID Equipo: ").append(idEquipo).append("\n");
        sb.append("Tipo de mantenimiento: ")
          .append(d.tipoMantenimiento != null ? d.tipoMantenimiento : "No especificado")
          .append("\n");
        SimpleDateFormat sdfFecha = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat fmtHora = new SimpleDateFormat("HH:mm:ss");
        sb.append("Fecha: ").append(sdfFecha.format(fecha)).append("\n");
        sb.append("Hora: ").append(fmtHora.format(hora)).append("\n\n");
        
        sb.append("Componentes intervenidos:\n");

        if (d.ram) sb.append("- RAM\n");
        if (d.procesador) sb.append("- Procesador\n");
        if (d.fuente) sb.append("- Fuente de poder\n");
        if (d.placaBase) sb.append("- Placa base\n");
        if (d.disipador) sb.append("- Disipador\n");

        if (d.almacenamiento != null) {
            if (d.almacenamiento.ssd) sb.append("- SSD\n");
            if (d.almacenamiento.hdd) sb.append("- HDD\n");
        }

        sb.append("\nPeriféricos:\n");

        if (d.perifericos != null) {
            if (d.perifericos.monitor) sb.append("- Monitor\n");
            if (d.perifericos.mouse) sb.append("- Mouse\n");
            if (d.perifericos.teclado) sb.append("- Teclado\n");
        }

        sb.append("\nDetalle:\n");
        sb.append(d.descripcion != null ? d.descripcion : "Sin descripción");

        return sb.toString();
    }
