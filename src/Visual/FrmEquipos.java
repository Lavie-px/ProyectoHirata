    private void BtnRegistroMantenimientoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnRegistroMantenimientoActionPerformed
        if (TxtIdEquipo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingresa un ID de equipo");
            return;
        }
        
        Detalles detalles = new Detalles();

        detalles.ram = ChkRam.isSelected();
        detalles.procesador = ChkProcesador.isSelected();
        detalles.fuente = ChkFuente.isSelected();
        detalles.placaBase = ChkPlaca.isSelected();
        detalles.disipador = ChkDisipador.isSelected();

        Detalles.Almacenamiento alm = new Detalles.Almacenamiento();
        alm.ssd = ChkSsd.isSelected();
        alm.hdd = ChkHdd.isSelected();
        detalles.almacenamiento = alm;

        Detalles.Perifericos per = new Detalles.Perifericos();
        per.monitor = ChkMonitor.isSelected();
        per.mouse = ChkMouse.isSelected();
        per.teclado = ChkTeclado.isSelected();
        detalles.perifericos = per;
        

        if (RdoPreventivo.isSelected()) {
            detalles.tipoMantenimiento = "PREVENTIVO";
        } else if (RdoCorrectivo.isSelected()) {
            detalles.tipoMantenimiento = "CORRECTIVO";
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona tipo de mantenimiento");
            return;
        }

        detalles.descripcion = TxtAreaDetalle.getText();
        
        ObjectMapper mapper = new ObjectMapper();

        String json = "";
        try {
            json = mapper.writeValueAsString(detalles);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al generar JSON");
            return;
        }
        
        String idEquipo = TxtIdEquipo.getText();
        String fecha = TxtFecha.getText();
        java.sql.Date fechaFormat = java.sql.Date.valueOf(fecha);
        CrudMantenimiento crud = new CrudMantenimiento();
        crud.guardar(idEquipo, json, fechaFormat);
    }//GEN-LAST:event_BtnRegistroMantenimientoActionPerformed

    private void BtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarActionPerformed
        
        String idEquipo = TxtIdEquipo.getText();
        String fecha = TxtFecha.getText();
        java.sql.Date fechaFormat = java.sql.Date.valueOf(fecha);
        if (idEquipo.isEmpty()||fecha.isEmpty()) {
           JOptionPane.showMessageDialog(this, "Campo Id o Fecha Vacios");
           return;
        }

        try {
            ResultadoMantenimiento res = crud.buscar(idEquipo, fechaFormat);

            if (res == null || res.json == null || res.json.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No hay mantenimiento registrado");
                return;
            }

            ObjectMapper mapper = new ObjectMapper();
            Detalles d = mapper.readValue(res.json, Detalles.class);

            limpiarUi();

            // reconstrucción UI
            ChkRam.setSelected(d.ram);
            ChkProcesador.setSelected(d.procesador);
            ChkFuente.setSelected(d.fuente);
            ChkPlaca.setSelected(d.placaBase);
            ChkDisipador.setSelected(d.disipador);

            ChkSsd.setSelected(d.almacenamiento != null && d.almacenamiento.ssd);
            ChkHdd.setSelected(d.almacenamiento != null && d.almacenamiento.hdd);

            ChkMonitor.setSelected(d.perifericos != null && d.perifericos.monitor);
            ChkMouse.setSelected(d.perifericos != null && d.perifericos.mouse);
            ChkTeclado.setSelected(d.perifericos != null && d.perifericos.teclado);

            if ("PREVENTIVO".equalsIgnoreCase(d.tipoMantenimiento)) {
                RdoPreventivo.setSelected(true);
            } else if ("CORRECTIVO".equalsIgnoreCase(d.tipoMantenimiento)) {
                RdoCorrectivo.setSelected(true);
            }
            TxtAreaDetalle.setText(d.descripcion);

            TxtAreaMantenimiento.setText(ResumenDetalle(idEquipo, d, (Date) res.fecha, res.hora));

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar mantenimiento");
        }
    }//GEN-LAST:event_BtnBuscarActionPerformed

